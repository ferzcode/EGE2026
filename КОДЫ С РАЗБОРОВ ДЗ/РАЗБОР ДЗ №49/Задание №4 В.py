from math import *  # подключаем модуль math для использования функции dist()


def center(cl):  # функция для нахождения центра кластера
    m = []  # (растояние, [точка])  # список для хранения суммы расстояний и соответствующей точки
    for p1 in cl:  # перебираем каждую точку кластера
        s = 0  # обнуляем сумму расстояний для текущей точки
        for p2 in cl:  # перебираем все точки кластера
            s += dist(p1, p2)  # прибавляем расстояние от p1 до p2
        m.append([s, p1])  # сохраняем сумму расстояний и саму точку
    return min(m)[1]  # выбираем точку с минимальной суммой расстояний (центр кластера)


cl1 = []  # список точек первого кластера
cl2 = []  # список точек второго кластера
cl3 = []  # список точек третьего кластера

c = 0  # счётчик всех точек (не используется для расчётов)
for s in open('Задание №4 В.txt'):  # читаем файл построчно
    x, y = map(float, s.replace(',', '.').split())  # заменяем запятую на точку и переводим координаты в числа
    c += 1  # увеличиваем счётчик
    if 8 < x < 16 and 10 < y < 20:  # проверяем, относится ли точка к первому кластеру
        cl1.append([x, y])  # добавляем точку в первый кластер
    if 10 < x < 18 and 20 < y < 30:  # проверяем, относится ли точка ко второму кластеру
        cl2.append([x, y])  # добавляем точку во второй кластер
    if 18 < x < 25 and 20 < y < 30:  # проверяем, относится ли точка к третьему кластеру
        cl3.append([x, y])  # добавляем точку в третий кластер

c1 = center(cl1)  # находим центр первого кластера
c2 = center(cl2)  # находим центр второго кластера
c3 = center(cl3)  # находим центр третьего кластера

# print(len(cl1)) # MAX - можно использовать для проверки, какой кластер с наибольшим количеством точек
# print(len(cl2))
# print(len(cl3)) # MIN - можно использовать для проверки, какой кластер с наименьшим количеством точек

Q1 = 0  # счётчик точек, находящихся на расстоянии <= 10 от центра кластера с наибольшим количеством точек
for p1 in cl1 + cl2 + cl3:  # перебираем все точки всех кластеров
    if dist(p1, c1) <= 10:  # если расстояние от точки до центра (c1) <= 10
        Q1 += 1  # увеличиваем счётчик
print(Q1)  # выводим количество таких точек

Q2 = 0  # счётчик точек, находящихся на расстоянии > 5 от центра кластера с наименьшим количеством точек
for p3 in cl1 + cl2 + cl3:  # перебираем все точки всех кластеров
    if dist(p3, c3) > 5:  # если расстояние от точки до центра (c3) > 5
        Q2 += 1  # увеличиваем счётчик
print(Q2)  # выводим количество таких точек
