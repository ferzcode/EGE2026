# print(dist(ac3, [0.0, 0.0]))  # расстояние от антицентра 3 до начала координат (для проверки)

Q1 = ac3[0]  # берём абсциссу антицентра выбранного кластера (предполагается самый удалённый)
Q2 = ac1[1]  # берём ординату антицентра выбранного кластера (предполагается ближайший)

