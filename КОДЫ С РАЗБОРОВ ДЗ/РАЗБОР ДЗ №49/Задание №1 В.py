from math import *  # подключаем модуль math для использования функции dist()


def anticenter(cl):  # функция для нахождения антицентра кластера
    m = []  # (растояние, [точка])  # список для хранения суммы расстояний и соответствующей точки
    for p1 in cl:  # перебираем каждую точку кластера
        s = 0  # обнуляем сумму расстояний для текущей точки
        for p2 in cl:  # перебираем все точки кластера
            s += dist(p1, p2)  # прибавляем расстояние от p1 до p2
        m.append([s, p1])  # сохраняем сумму расстояний и саму точку
    return max(m)[1]  # выбираем точку с максимальной суммой расстояний (антицентр)


cl1 = []  # список точек первого кластера
cl2 = []  # список точек второго кластера
cl3 = []  # список точек третьего кластера

for s in open('Задание №1 В.txt'):  # читаем файл построчно
    x, y = map(float, s.replace(',', '.').split())  # заменяем запятую на точку и переводим координаты в числа

    if 8 < x < 16 and 10 < y < 20:  # проверяем, относится ли точка к первому кластеру
        cl1.append([x, y])  # добавляем точку в первый кластер
    if 10 < x < 18 and 20 < y < 30:  # проверяем, относится ли точка ко второму кластеру
        cl2.append([x, y])  # добавляем точку во второй кластер
    if 18 < x < 25 and 20 < y < 30:  # проверяем, относится ли точка к третьему кластеру
        cl3.append([x, y])  # добавляем точку в третий кластер

ac1 = anticenter(cl1)  # находим антицентр первого кластера
ac2 = anticenter(cl2)  # находим антицентр второго кластера
ac3 = anticenter(cl3)  # находим антицентр третьего кластера

